#include<stdio.h>

int main(){
    int a,b;

    // a<<b(Left shift operator)
    printf("Enter first operand=");
    scanf("%d",&a);
    printf("Enter second operand=");
    scanf("%d",&b);

    printf("a<<b=%d\n",a<<b);

    // a>>b(Right shift operator)
    printf("Enter first operand=");
    scanf("%d",&a);
    printf("Enter second operand=");
    scanf("%d",&b);

    printf("a>>b=%d",a>>b);

    return 0;
}

    